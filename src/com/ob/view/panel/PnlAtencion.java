    private void editAtencion() {
        Atencion a = tblAtencion.getSelected();
        Util.i(a.getId());
        Util.i(a.getProducto().getNombre());
        pnlEditarAtencion.setAtencion(a);
        Util.changeCard(pnlBuscar, "Editar");
    }
    
